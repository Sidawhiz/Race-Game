void Map::LoadMap(){
    int tile;
    for (int y = 0; y < 20; y++)
    {
        for (int x = 0; x < 25; x++)
        {
            tile = roughmaze[y][x];
            Game::AddTile(tile, x * 32, y * 32);
            if (tile == 2)
            {
                Game::AddCollectible(tile, x * 32, y * 32);
            }
        }
    }
}
